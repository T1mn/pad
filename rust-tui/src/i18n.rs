use std::collections::HashMap;
use std::sync::LazyLock;

#[derive(Clone, Copy, PartialEq, Debug)]
pub enum Locale {
    ZhCN,
    ZhTW,
    En,
    Ja,
    De,
    Fr,
}

impl Locale {
    pub fn from_str(s: &str) -> Self {
        match s {
            "zh-cn" | "zh_CN" => Locale::ZhCN,
            "zh-tw" | "zh_TW" => Locale::ZhTW,
            "en" | "en-us" | "en_US" => Locale::En,
            "ja" => Locale::Ja,
            "de" => Locale::De,
            "fr" => Locale::Fr,
            _ => Locale::En,
        }
    }

    pub fn as_str(&self) -> &'static str {
        match self {
            Locale::ZhCN => "zh-cn",
            Locale::ZhTW => "zh-tw",
            Locale::En => "en",
            Locale::Ja => "ja",
            Locale::De => "de",
            Locale::Fr => "fr",
        }
    }

    pub fn display_name(&self) -> &'static str {
        match self {
            Locale::ZhCN => "简体中文",
            Locale::ZhTW => "繁體中文",
            Locale::En => "English",
            Locale::Ja => "日本語",
            Locale::De => "Deutsch",
            Locale::Fr => "Français",
        }
    }

    #[allow(dead_code)]
    pub fn next(&self) -> Self {
        match self {
            Locale::ZhCN => Locale::ZhTW,
            Locale::ZhTW => Locale::En,
            Locale::En => Locale::Ja,
            Locale::Ja => Locale::De,
            Locale::De => Locale::Fr,
            Locale::Fr => Locale::ZhCN,
        }
    }
}

pub fn t(locale: Locale, key: &str) -> &str {
    match locale {
        Locale::ZhCN => ZH_CN.get(key).copied().unwrap_or(key),
        Locale::ZhTW => ZH_TW.get(key).copied().unwrap_or(key),
        Locale::En => EN.get(key).copied().unwrap_or(key),
        Locale::Ja => JA.get(key).copied().unwrap_or(key),
        Locale::De => DE.get(key).copied().unwrap_or(key),
        Locale::Fr => FR.get(key).copied().unwrap_or(key),
    }
}

// Macro to reduce repetition: defines a LazyLock<HashMap<&str, &str>> from pairs.
macro_rules! locale_map {
    ($name:ident, $( $key:expr => $val:expr ),+ $(,)? ) => {
        static $name: LazyLock<HashMap<&str, &str>> = LazyLock::new(|| {
            HashMap::from([ $( ($key, $val), )+ ])
        });
    };
}

